// Definición de una cadena con espacios en blanco al inicio y al final
const text = "   Hola, mundo!   ";

// Usando el método trimStart() para eliminar los espacios en blanco del inicio
const trimmedStart = text.trimStart();
console.log(trimmedStart); // Resultado: "Hola, mundo!   "

// Usando el método trimEnd() para eliminar los espacios en blanco del final
const trimmedEnd = text.trimEnd();
console.log(trimmedEnd); // Resultado: "   Hola, mundo!"

