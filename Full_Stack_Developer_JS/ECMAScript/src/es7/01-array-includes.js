// Comprobando si un elemento está presente en un array usando .includes()
let numbers = [1, 2, 3, 4, 5];

console.log(numbers.includes(2)); // Salida: true (2 está en el array)

console.log(numbers.includes(8)); // Salida: false (8 no está en el array)

let names = ["john", "james", "fred", "randy"];

console.log(names.includes("john")); // Salida: true ("john" está en el array)

console.log(names.includes("John")); // Salida: false ("John" no está en el array)
