// Ejemplo 1: Valor de dinero en dólares
const totalPrice = 1_500_000; // Equivalente a $1,500,000
console.log("Precio total:", totalPrice);

// Ejemplo 2: Identificador numérico con guiones bajos
const orderId = 2022_08_001; // Equivalente a 202208001
console.log("Número de orden:", orderId);

// Ejemplo 3: Uso en valores hexadecimales
const hexColor = 0xFF_00_FF; // Equivalente a 0xFF00FF
console.log("Color hexadecimal:", hexColor.toString(16));

// Ejemplo 4: Uso en valores binarios
const binaryValue = 0b1010_1100_0101; // Equivalente a 0b101011000101
console.log("Valor binario:", binaryValue.toString(2));

// Ejemplo 5: Número grande para representar segundos
const totalSeconds = 1_000_000_000; // Equivalente a 1000000000 segundos
console.log("Total de segundos:", totalSeconds);

// Ejemplo 6: Valor científico con guiones bajos
const scientificValue = 1_234.56_78e-2; // Equivalente a 12.345678
console.log("Valor científico:", scientificValue);
