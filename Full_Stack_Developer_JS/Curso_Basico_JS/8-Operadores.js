// En JavaScript, existen diferentes tipos de operadores que se utilizan para realizar operaciones en variables y valores.

// Operadores aritméticos:
console.log(5 + 3); // Salida: 8
console.log(10 - 4); // Salida: 6
console.log(3 * 5); // Salida: 15
console.log(10 / 2); // Salida: 5
console.log(15 % 7); // Salida: 1

// Operadores de asignación:
var x = 10;
x += 5; // Equivalente a x = x + 5
console.log(x); // Salida: 15

// Operadores de comparación:
console.log(5 > 3); // Salida: true
console.log(10 <= 4); // Salida: false
console.log(3 === '3'); // Salida: false (estrictamente igual)
console.log(10 != 5); // Salida: true

// Operadores lógicos:
console.log(true && false); // Salida: false
console.log(true || false); // Salida: true
console.log(!true); // Salida: false

// Operadores de concatenación:
var mensaje = "Hola, ";
var nombre = "Juan";
console.log(mensaje + nombre); // Salida: "Hola, Juan"

// Operador ternario:
var edad = 18;
var mensaje = edad >= 18 ? "Eres mayor de edad" : "Eres menor de edad";
console.log(mensaje); // Salida: "Eres mayor de edad"

// Operador typeof:
console.log(typeof 42); // Salida: "number"
console.log(typeof "Hola"); // Salida: "string"
console.log(typeof true); // Salida: "boolean"

